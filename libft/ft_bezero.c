#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*p;
	int	i;

	*p = (char *)s;
	i = 0;
	while (n > 0)
	{
		p[i] = '0';
		i++;
		n--;
	}
	return (s);
}
